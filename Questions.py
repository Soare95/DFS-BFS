# If you know a solution is not far from the root of the tree:
# BFS - it start searching closest nodes to the parent node

# If the tree is very deep and solutions are rare:
# BFS - BFS takes less time with a very deep tree and because solutions are rare, DFS will take more time to return
# to the last node

# If the tree is very wide:
# DFS - DFS will need less memory

# If solutions are frequent but located deep in the tree:
# DFS

# Determining whether a path exists between two nodes:
# DFS - this is why DFS was build for

# Finding the shortest path:
# BFS
